    // untuk menampilkan data uang masuk
    public void tampilkan(){
        
        uang_masuk();
        sisa_uang();
        
        // untuk menghapus data sebelumnya ketika data yang baru di tampilkan
        // jika tidak data akan terduplikat di tabel
        int row = tabelMasuk.getRowCount();
        for(int i = 0 ; i < row ; i++){
            model.removeRow(0);
        }
        
        this.TFkode.setVisible(false);
        String tempCari = TFCariMasuk.getText();
        int no = 1;
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/uangkas","root","");
            ResultSet rs = cn.createStatement().executeQuery("select * from uangmasuk WHERE pembayar LIKE '%"+tempCari+"%'");
            
            while(rs.next()){
                //String data [] = {rs.getString,rs.getString(1),rs.getString(2),rs.getString(3)};
                model.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(UangKas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
